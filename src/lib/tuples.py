#learning Tuples/couples

my_tuple = (1,2,3,2)
print(my_tuple)
print(type(my_tuple))

#Indexing of the tuple

print( my_tuple[3])

print(len(my_tuple))

#Count the number of occurences of the object in the tuple
print(my_tuple.count(1))
print(my_tuple.count(2))
print(my_tuple.count(3))

#Returns the very first index of the object in the tuple
print(my_tuple.index(1))
print(my_tuple.index(2))